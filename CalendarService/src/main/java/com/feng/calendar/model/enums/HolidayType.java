package com.feng.calendar.model.enums;

/**
 * Enum representing the type of holiday
 */
public enum HolidayType {
	/**
	 * National holiday observed across the entire country
	 */
	NATIONAL,

	/**
	 * Regional holiday observed in specific regions or states
	 */
	REGIONAL,

	/**
	 * Religious holiday observed by specific religious communities
	 */
	RELIGIOUS,

	/**
	 * Cultural holiday based on cultural traditions
	 */
	CULTURAL,

	/**
	 * Custom holiday defined by business calendar rules
	 */
	CUSTOM
}
